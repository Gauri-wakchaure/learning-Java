package LoopingStatement;

public class TestForLoop {

	public static void main(String[] args) 
	
	{
		System.out.println("Print 1 to 5 Numbers :");
       for(int i=1; i<=5; i++)
       {
    	   System.out.println(i + " ");      
    	   
	   }

	System.out.println();
	System.out.println("Print 1 to 5 Numbers in Reverse Order :");
    for(int i=5; i>=1; i--)
    {
 	   System.out.println(i + " ");      
 	   }
	 }


}
