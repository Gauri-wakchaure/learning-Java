package wrapperclass;

public class AutoBoxingTask {

	public static void main(String[] args) {
 
		// Creating Autoboxing of Primitive datatypes
		byte b=10;
		Byte b1=b;
		System.out.println("B1 is :"+b1);
		
		short s=20;
		Short s1=s;
		System.out.println("S1 is :"+s1);
		
		int i=30;
		Integer I1=i;
		System.out.println("I1 is :"+I1);
		
		long l=50;
		Long l1=l;
		System.out.println("L1 is :"+l1);
		
		float f=60.0f;
		Float f1=f;
		System.out.println("F1 is :"+f1);
		
		double d=70.00;
		Double d1=d;
		System.out.println("D1 is :"+d1);
		
		char c='s';
		Character c1=c;
		System.out.println("C1 is :"+c1);
		
		boolean b3=true;
		Boolean b4=b3;
		System.out.println("B4 is :"+b4);
		
		
		

	}
}
